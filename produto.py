class Produto:

    def __init__(self):
        self.prod
        self.prod_cad
        self.prod_sel
        self.prod_cad_escolha
        
    def set_prod(self, Prod):
        self.prod = Prod

    def get_prod(self):
        return self.prod

    def set_prod_cad(self, ProdCad):
        self.prod_cad = ProdCad

    def get_prod_cad(self):
        return self.prod_cad

    def set_prod_sel(self, ProdSel):
        self.prod_sel = ProdSel

    def get_prod_sel(self):
        return self.prod_sel

    def set_prod_cad_escolha(self, ProdCadEscolha):
        self.prod_cad_escolha = ProdCadEscolha

    def get_prod_cad_escolha(self):
        return self.prod_cad_escolha